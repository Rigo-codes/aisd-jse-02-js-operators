// this script defines variables, performs basic arithmetic 
// operations. and compares values in javascript.



// Define variables for arithmetic operations.

const a = 10;

// Second variable to use in operations.

const b = 5;

// Perform operations.

const sum = a + b;

const difference = a - b;

const product = a * b;

const quotient = a / b;

// Log results

console.log("Sum: ", sum)
console.log("Difference: ", difference)
console.log("Product: ", product)
console.log("Quotient: ", quotient)


const isGrater = a > b;
const isEqual = a === b;

// Log comparisons.

console.log("Is a greater than b?", isGrater)

console.log("Is a equal to b?", isEqual);
